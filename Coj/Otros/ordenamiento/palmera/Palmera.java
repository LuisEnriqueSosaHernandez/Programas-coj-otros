public class Palmera{
private int numeroCocos;
private float altura;
public Palmera(int a,float b){
numeroCocos=a;
altura=b;
}
public void setNumeroCocos(int a){
numeroCocos=a;
}
public void setAltura(float a){
altura=a;
}
public int getNumeroCocos(){
return numeroCocos;
}
public float getAltura(){
return altura;
}

}